      <section id="overview">
        <h1>What is the conference about?</h1>
        <p>Here in San Francisco, you are just as likely to find bacon on the table, as you are to find chicharrones or choila. At the heart of this city&rsquo;s diverse food culture are the enterprising culinary entrepreneurs from all walks of life. As local, small-batch, and artisanal foods trends are capturing everyone&rsquo;s attention, the question remains, can you make it making great food? Since its inception in 2009, La Cocina&rsquo;s Food &amp; Entrepreneurship conference has looked at how we can best cultivate food entrepreneurs and how that in turn results in thriving and inclusive communities. We hope that you will join us on August 17th for a Sunday Thoughtluck discussing the future of food systems, alongside food entrepreneurs, restaurateurs, city officials, culinary experts, and community organizations from all over the world. 
        Alongside thoughtful dialog and hands on workshops, there will be an Art+Food Expo, and of course, oodles of eats!</p>
      </section>

      <section id="details">
        <h1>Conference sneak peek</h1>
        <ol>
          <li><em>The Value of Ethnic Food:</em> Join <a href="https://www.facebook.com/sanfranciscochronicle">San Francisco Chronicle&rsquo;s</a> Jonathan Kauffman and <a href="https://www.facebook.com/haparamen">Hapa Ramen&rsquo;s</a> Richie Nakano, along with other panelists, in this engaging discussion on paying a fair price for ethnic food.</li>
          <li><em>Making Made in SF Real:</em> SF Planning and <a href="http://www.spur.org/">SPUR</a> will present their finding on what it would take to make San Francisco a hub for food manufacturing, and invite the audience to brainstorm solutions.</li>
          <li><em>Cities + Festival + Foods:</em> A m&eacute;lange of ethnicities and cultures discussing how they&rsquo;re using food + food festivals as a tool to bring communities together in their home cities. We&rsquo;ll have food festival hosts from Brazil, New Zealand, and Sweden!</li>
          <li><em>Packaging + Branding:</em> interactive session with <a href="http://www.ideo.com/">IDEO</a> working on prototyping for food packaging, as well as insight into what makes a strong and successful food brand.</li>
          <li><em>Investment Readiness for Food Businesses:</em> An interactive workshop hosted by Friday Consulting will talk about investment readiness and help producers work on their personal pitches.</li>
        </ol>

        <p>Our newest addition this year is the Art + Food Expo, which will be running alongside the lunch break; it will be a chance for the conference attendees to interact with ideas around food and city spaces through art and activities. We will have <a href="http://www.madelenefarin.com/">Madelene Farin</a> with her Foodscapes exhibition, <a href="http://www.nomadgardens.org/">Nomad Gardens</a> holding a fun, hands-on activity, Eat Bugs! by <a href="http://www.donbugito.com/">Don Bugito</a>, amongst others!</p>

        <p>And, it wouldn&rsquo;t be a La Cocina event without fantastic food now would it? We will have &ldquo;Around the World in a Lunchbox&rdquo; presented by <a href="http://andapiroshki.com/">Anda Piroshki</a>, <a href="http://www.ejisethiopian.com/">Eji&rsquo;s Ethiopian</a>, <a href="http://www.saldevidagourmet.com/">Sal De Vida</a>, and <a href="http://www.aedansf.com/">Aedan Fermented Foods</a>. We will also be serving breakfast, have enough caffeine to power you through a Sunday Conference, an ice cream social (why not?), and a happy hour to wrap things up.</p>
