          <li>Organize attendee lists</li>
          <li>Organize name badges</li>
          <li>Ensure all registration staff have highlighters &amp; pens (La Cocina team to provide)</li>
          <li>Put up signage</li>
        </ul>

        <h2>Registration</h2>
        <ul class="text">
          <li>Check attendees off lists</li>
          <li>Accept payment for walk-ins</li>
          <li>Hand out namebadges</li>
          <li>Direct attendees to meeting rooms, restrooms, etc.</li>
          <li>Keep notes of concerns/issues/follow-up questions</li>
          <li>Sell merchandise?</li>
          <li>Access Control/brunch &ndash; make sure attendees have ticket to be admitted to brunch</li>
        </ul>

        <h2>Photographer</h2>
        <ul class="text">
          <li>Take &ldquo;scenic&rdquo; photos of the room setup, food displays, etc.</li>
          <li>Take photos of sponsor banners, logos, product placement</li>
          <li>Take candids photos of guests at the events</li>
          <li>Photograph chefs at their stations at Sunday brunch</li>
          <li>Deliver CD of images to La Cocina staff after event</li>
        </ul>

        <h2>Food &amp; Beverage / Culinary Support</h2>
        <ul class="text">
          <li>Set up food prep areas</li>
          <li>Assist chefs with food prep</li>
          <li>Set up meal location(s) with tables, linens, serviceware</li>
          <li>Ice down beverages</li>
          <li>Assist with beverage prep as needed</li>
          <li>Pick up and set up water stations</li>
          <li>Replenish water stations in meal location(s), as needed</li>
          <li>During food events, assist with flow of guests, bussing/trash</li>
          <li>After food events, assist with cleanup/strike</li>
        </ul>

        <h2>Floater @ Brunch</h2>
        <ul class="text">
          <li>During food events, assist with flow of guests, bussing/trash</li>
          <li>Ensure chefs have support for replenishment, cleanup, etc.</li>
          <li>Explain food ingredients in case of dietary restrictions</li>
          <li>Manage lines / direct guests to areas where there are not lines</li>
        </ul>

        <h2>Speaker Management / Technical Coordinator</h2>
        <ul class="text">
          <li>Load presentations on main laptop</li>
          <li>Add/edit presentations if needed</li>
          <li>Work with AV tech on run-of-show</li>
          <li>Place table tents for panelists before each session</li>
        </ul>

        <h2>Mic Runner / Audience Q&amp;A</h2>
        <ul class="text">
          <li>Identify people in audience who have questions</li>
          <li>Hand microphone to audience member and retrieve after question has been asked</li>
        </ul>

